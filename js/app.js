// ─────────────────────────────────────────────────────────────
//  CANVAS PIXELATION
//  Draws the image at 1/blockSize then scales back up so each
//  original pixel becomes a chunky blockSize×blockSize square.
// ─────────────────────────────────────────────────────────────
function pixelateImage(img, blockSize) {
  if (img.src.startsWith('data:')) return;
  const w = img.naturalWidth;
  const h = img.naturalHeight;
  if (!w || !h) return;

  const canvas = document.createElement('canvas');
  canvas.width  = w;
  canvas.height = h;
  const ctx = canvas.getContext('2d');
  ctx.imageSmoothingEnabled = false;

  const sw = Math.max(1, Math.round(w / blockSize));
  const sh = Math.max(1, Math.round(h / blockSize));
  ctx.drawImage(img, 0, 0, sw, sh);
  ctx.drawImage(canvas, 0, 0, sw, sh, 0, 0, w, h);

  img.src = canvas.toDataURL('image/png');
}

