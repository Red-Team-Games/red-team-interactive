function flash(cb) {
  const f = document.getElementById('flash');
  f.style.transition = 'opacity 0.08s';
  f.style.opacity = '1'; f.style.pointerEvents = 'all';
  setTimeout(() => {
    f.style.opacity = '0.1';
    setTimeout(() => {
      f.style.opacity = '0.9';
      setTimeout(() => { f.style.opacity = '0'; f.style.pointerEvents = 'none'; if (cb) cb(); }, 80);
    }, 60);
  }, 80);
}

function fadeToBlack(cb) {
  const f = document.getElementById('flash');
  f.style.transition = 'opacity 0.2s';
  f.style.opacity = '1'; f.style.pointerEvents = 'all';
  setTimeout(() => { if (cb) cb(); }, 220);
}

function fadeFromBlack(cb) {
  const f = document.getElementById('flash');
  f.style.transition = 'opacity 0.3s';
  f.style.opacity = '0'; f.style.pointerEvents = 'none';
  if (cb) setTimeout(cb, 320);
}

function toHome() {
  setHash('');
  fadeToBlack(() => { showScreen('home-screen'); fadeFromBlack(); });
}

